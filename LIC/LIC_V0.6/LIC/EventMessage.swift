import Foundation
//import ObjectMapper

class EventMessage : Mappable{
    
    
    
    
    var id:Int!
    var type:Int!
    var category:Int!
    var userId:Int!
    var eventTime:NSTimeInterval!
    var value:String!
    var ebikeId:Int!
    var latitude:CLLocationDegrees!
    var longitude:CLLocationDegrees!
    var bdAddr:String!
    var speed:Double!
    var course:Double!
    var insertTime:NSTimeInterval!
    var readFlag:Int!
    var locationTime:NSTimeInterval!
    var accEventFlag:Int!
    var weather:String!
    var weatherCity:String!
    var windstemp:String!
    var temperature:String!
    var pm25:String!
    var dangerIndex:Int!
    var des:String!
    var week:Int!
    

    
    required init(){}
    
    func mapping(map: Map) {
        
        id <= map["id"]
        type <= map["type"]
        category <= map["category"]
        userId <= map["userId"]
        eventTime <= map["eventTime"]
        value <= map["value"]
        ebikeId <= map["ebikeId"]
        latitude <= map["latitude"]
        longitude <= map["longitude"]
        bdAddr <= map["bdAddr"]
        speed <= map["speed"]
        course <= map["course"]
        insertTime <= map["insertTime"]
        readFlag <= map["readFlag"]
        locationTime <= map["locationTime"]
        weather <= map["weather"]
        weatherCity <= map["weatherCity"]
        windstemp <= map["wind"]
        temperature <= map["temperature"]
        pm25 <= map["pm25"]
        dangerIndex <= map["dangerIndex"]
        des <= map["des"]
        week <= map["week"]
        
    }
    

    

}
    