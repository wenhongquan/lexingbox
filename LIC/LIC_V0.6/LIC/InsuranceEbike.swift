import Foundation
//import ObjectMapper

class  InsuranceEbike:Mappable{
    required init(){}
    
    var id:Int?
    var code:String?
    var gpsId:Int?
    var insertTime:NSTimeInterval?
    var model:String?
    var motorNo:String?
    var color:String?
    var brand:String?
    var insertUserId:Int?
    var insuranceUserId:Int?
    var factoryTime:NSTimeInterval?
    var buyingPrice:Double?
    
    
    func mapping(map: Map) {
    
        id <= map["id"]
        code <= map["code"]
        gpsId <= map["gpsId"]
        insertTime <= map["insertTime"]
        model <= map["model"]
        motorNo <= map["motorNo"]
        color <= map["color"]
        brand <= map["brand"]
        insertUserId <= map["insertUserId"]
        insuranceUserId <= map["insuranceUserId"]
        factoryTime <= map["factoryTime"]
        buyingPrice <= map["buyingPrice"]
       
    
    }
}