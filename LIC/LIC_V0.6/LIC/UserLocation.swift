import Foundation

class UserLocation:Mappable {
    
    var province:String?
    var city:String?
    var area:String?
    
    
    
    required init(){}
    func mapping(map: Map) {
        province <= map["province"]
        city <= map["city"]
        area <= map["area"]
    }
}